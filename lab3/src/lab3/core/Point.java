package lab3.core;

public class Point 
{
    private final double _x;
    private final double _y;

    public Point(double x, double y)
    {
        _x = x;
        _y = y;
    }
    
    public double getX()
    {
        return _x;
    }
    
    public double getY()
    {
        return _y;
    }
}
